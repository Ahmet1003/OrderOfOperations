public class Main {
    public static void main(String[] args) {
        int x = 10 + 2 * 3;
        System.out.println(x);

        int y = (10 + 2) * 3;
        System.out.println(y);

        // So the order of operations are as follows;

        // 1. ()
        // 2. *  /
        // 3. +  -
    }
}